package com.array;

public class multi_array {
	public static void main(String[] args) {
		
		int a[][]= {{20,30},{40,50}};
		for (int i = 0; i < 2; i++) {
			System.out.println("value of i :"+i);
		
		
		for (int j = 0; j < 2; j++) {
			System.out.println("value of j:"+j);
	
		
		System.out.println(a[i][j]);
	    	
		}
	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		}
	}
	}


